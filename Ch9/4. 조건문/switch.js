// 입력한 변수가 과일(fruit)인지 야채(vegetable)인지 판별

let input = "cup";

switch (input){
    case "melon":
        console.log("fruit");
        break;

    case "apple":
        console.log("fruit");
        break;

    case "banana":
        console.log("fruit");
        break;

    case "carrot":
        console.log("vegetable");
        break;

    default:
        console.log("야채도 과일도 그 무엇도 아닙니다.");
}